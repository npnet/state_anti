#include "swap_hl.h"

//******************************************************************************            
//name:             u16_t swap_hl8(u16_t val)     
//introduce:        高低8位互换       
//parameter:        none                
//return:           none         
//author:           Luee                                              
//******************************************************************************
u16_t swap_hl8(u16_t data)
{
	u16_t result;
	result = ((data << 8)&0xFF00) | ((data >> 8)&0x00FF);
	return result;
}