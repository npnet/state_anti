static void prvInvokeGlobalCtors(void)
{
    extern void (*__init_array_start[])();
    extern void (*__init_array_end[])();

    size_t count = __init_array_end - __init_array_start;
    for (size_t i = 0; i < count; ++i)
        __init_array_start[i]();
}

static void prvThreadEntry(void *param)
{
	int ret;
	for (int i = 0; i < 5; i++)
    {
        OSI_LOGI(0, "before fibo_ota_handle %d", i);
        fibo_taskSleep(2000);
    }
	
	ret = fibo_ota_handle(data, sizeof(data));
	if(ret < 0){
		OSI_LOGI(0, "fibo_ota_handle failed.");
	}
	
	for (int i = 0; i < 1000000; i++)
    {
        OSI_LOGI(0, "after fibo_ota_handle %d", i);
        fibo_taskSleep(2000);
    }
	fibo_thread_delete();
}

void * appimg_enter(void *param)
{
    OSI_LOGI(0, "application image enter, param 0x%x", param);

    prvInvokeGlobalCtors();

    fibo_thread_create(prvThreadEntry, "mythread", 1024*16, NULL, OSI_PRIORITY_NORMAL);
    return 0;
}

void appimg_exit(void)
{
    OSI_LOGI(0, "application image exit");
}
